<template>
    <div @click="$emit('taskComplete',task)"
        class="task" :class="stateClass">
        <span @click.stop="$emit('taskDeleted',task)" class="close">x</span>
        <p> {{task.name }}</p>
    </div>
</template>

<script>
export default {
    //define que o tipo de informação recebida será de um objeto, no caso desta aplicação
    //os objetos serão os itens da lista, que possui o name e o pending
    props:{
        task: {type: Object, Required: true}
    },
    computed:{
        stateClass(){
            return{
              pending: this.task.pending, 
              done: !this.task.pending 
            }
        }
    }
}
</script>

